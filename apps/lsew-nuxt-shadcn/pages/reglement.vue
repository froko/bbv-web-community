    <h1>Reglement</h1>
    <h2>Zweck</h2>
    <p>
      Der "Luzerner Solo- und Ensemblewettbewerb" (LSEW) für Blas – und Schlaginstrumente hat zum Ziel
      Bläserinnen/Bläser sowie Perkussionistinnen/Perkussionisten zu fördern und ihnen Gelegenheit zu geben, ihr Können
      im Rahmen eines friedlichen Wettspiels zu präsentieren.
    </p>
    <p>Der Einfachheit halber wird in diesem Reglement nur die männliche Schreibweise verwendet.</p>
    <h2>Organisation</h2>
    <p>Die Organisation und der Ablauf des LSEW stehen unter der Aufsicht des OK LSEW.</p>
    <p>
      Der LSEW findet grundsätzlich alljährlich statt. Das OK LSEW bestimmt das jeweilige Datum und den Austragungsort.
    </p>
    <h2>Teilnahmebedingungen</h2>
    <p>
      Teilnahmeberechtigt sind Bläser und Perkussionisten die im Kanton Luzern festen Wohnsitz haben, Mitglied eines
      Luzerner Musikvereins sind oder an einer Luzerner Musikschule unterrichtet werden.
    </p>
    <p>
      Ausgeschlossen sind diplomierte Musiker oder Studierende im Bachelorlehrgang, sowie Musiklehrer, die mehr als 30%
      ihres Einkommens mit Musikunterricht verdienen.
    </p>
    <h2>Wettbewerbskategorien</h2>
    <p>Der "Luzerner Solo- und Ensemblewettbewerb" umfasst die folgenden Wettbewerbskategorien:</p>
    <ul>
      <li class="flex flex-col lg:flex-row">
        <b class="basis-1/4">Kategorie B</b>
        <span class="basis-1/2">Luzerner Meisterschaft</span>
        <span class="basis-1/4">ab 16-jährig</span>
      </li>
      <li class="flex flex-col lg:flex-row">
        <b class="basis-1/4">Kategorie C</b>
        <span class="basis-1/2">Luzerner Juniorenmeisterschaft</span>
        <span class="basis-1/4">13 bis 15-jährig</span>
      </li>
      <li class="flex flex-col lg:flex-row">
        <b class="basis-1/4">Kategorie D</b>
        <span class="basis-1/2">Luzerner Beginnermeisterschaft</span>
        <span class="basis-1/4">bis und mit 12-jährig</span>
      </li>
      <li class="flex flex-col lg:flex-row">
        <b class="basis-1/4">Kategorie E</b>
        <span class="basis-3/4">Luzerner Ensemblemeisterschaft</span>
      </li>
    </ul>
    <p>
      Das OK LSEW behält sich das Recht vor, die Einteilung der Kategorien zu ändern, zusammenzulegen oder aufzuteilen,
      sofern dies aufgrund der Anmeldungen notwendig ist, um einen optimalen Wettbewerbsablauf zu gewährleisten.
    </p>
    <p>
      Entscheidend für die Kategorienzuteilung der Kategorien B, C und D ist das Alter des Solisten am ersten
      Wettbewerbstag.
    </p>
    <h2>Jury</h2>
    <p>Die Jury setzt sich aus kompetenten Musikern zusammen. Ihre Wahl erfolgt durch die MUKO des OK LSEW.</p>
    <h2>Bewertung</h2>
    <p>
      Jedes Jurymitglied bewertet selbständig und verfügt über 100 Punkte. Es hat keine Sichtverbindung mit den
      Wettbewerbsteilnehmern (Kat. D und die Instrumentengruppen der Schlaginstrumente haben eine offene Jury). Die
      erreichten Punktzahlen werden zusammengezählt und anschliessend durch die Anzahl Jurymitglieder dividiert. Der
      Punktedurchschnitt beträgt demnach im Maximum 100 Punkte. Die Jurymitglieder dürfen sich absprechen.
    </p>
    <p>
      Es werden minimal drei, maximal jedoch die Hälfte der Teilnehmer pro Kategorie rangiert, sofern sie mindestens 80
      Punkte erreicht haben. Die übrigen erhalten keine Rangierung
    </p>
    <p>Die Entscheide der Jury sind endgültig und nicht anfechtbar.</p>
    <h2>Wettwewerbskontrollen</h2>
    <p>
      Der LSEW steht unter Aufsicht der Musikkommission (MUKO), die den korrekten Ablauf der Veranstaltung gemäss
      vorliegendem Reglement überwacht.
    </p>
    <h2>Schlussbestimmungen</h2>
    <p>
      Das OK LSEW behält sich das Recht vor, eine Anmeldung zurückzuweisen, falls sie nicht dem vorliegenden Reglement
      entspricht. Es ist ebenfalls dazu berechtigt, den Zeitpunkt des Wettbewerbs abzuändern oder auf eine Durchführung
      zu verzichten, wenn zwingende Gründe dies erfordern. In diesem letzteren Fall würde das Startgeld rückerstattet.
    </p>
    <p>
      Mit der Anmeldung unterzieht sich jeder Teilnehmer dem vorliegenden Reglement. Wer es verletzt, droht die
      Disqualifikation. Das OK LSEW kann in Ausnahmefällen und in Absprache mit der MUKO LSEW Abweichungen vom
      vorliegenden Reglement bewilligen.
    </p>
    <p>
      Mit der Anmeldung erklären sich die Teilnehmer damit einverstanden, dass Bilder von Ihnen, welche im Rahmen des
      Wettbewerbs gemacht wurden, durch das OK LSEW veröffentlicht werden können. Der Teilnehmer kann seine erklärte
      Einwilligung jederzeit schriftlich widerrufen.
    </p>
    <p>Das vorliegende Reglement ersetzt und entkräftet alle vorhergehenden Reglemente.</p>
